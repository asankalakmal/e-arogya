package org.apache.struts.ehr.medical_records.model;

public class Address {

	private String city_1;
	private String country;
	private String house_number;
	private String city_2;
	private String street;
	
	public String getCity_1() {
		return city_1;
	}
	public void setCity_1(String city_1) {
		this.city_1 = city_1;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getHouse_number() {
		return house_number;
	}
	public void setHouse_number(String houseNumber) {
		house_number = houseNumber;
	}
	public String getCity_2() {
		return city_2;
	}
	public void setCity_2(String city_2) {
		this.city_2 = city_2;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	
}
